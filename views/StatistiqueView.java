    private void initialiserStats() {

        // Valeurs par défaut à l'ouverture
        lblResultatJour.setText("CA du jour : 0 FCFA");
        lblResultatPeriode.setText("CA période : 0 FCFA");

        // Optionnel : dates par défaut
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        String aujourdHui = LocalDate.now().format(formatter);

        txtDateJour.setText(aujourdHui);
        txtDateDebut.setText(aujourdHui);
        txtDateFin.setText(aujourdHui);
        
        modelTopProduits = new DefaultTableModel(
        new Object[]{"Produit", "Quantité vendue",  "Stock", "Seuil", "État"}, 0
    );
    tableTopProduits.setModel(modelTopProduits);

}

    
