    private void btnCalculerJourActionPerformed(java.awt.event.ActionEvent evt) {                                                
        // TODO add your handling code here:
        try {
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate date = LocalDate.parse(txtDateJour.getText(), formatter);

            double ca = calculChiffreAffairesJour(date);
            lblResultatJour.setText("CA du jour : " + formatMontant(ca));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Date invalide (dd/MM/yyyy)");
        }
    }                                               

    private void btnCalculPeriodeActionPerformed(java.awt.event.ActionEvent evt) {                                                 
        // TODO add your handling code here:
        try {
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");

            LocalDate debut = LocalDate.parse(txtDateDebut.getText(), formatter);
            LocalDate fin = LocalDate.parse(txtDateFin.getText(), formatter);

            if (fin.isBefore(debut)) {
                JOptionPane.showMessageDialog(this, "La date de fin doit être après la date de début");
                return;
            }

            double ca = calculChiffreAffairesPeriode(debut, fin);
            lblResultatPeriode.setText("CA période : " + formatMontant(ca));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Dates invalides (dd/MM/yyyy)");
        }
    }                                                

    private void btnTopProduitsActionPerformed(java.awt.event.ActionEvent evt) {                                               
        // TODO add your handling code here:
        try {
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");

            LocalDate debut = LocalDate.parse(txtDateDebut.getText(), formatter);
            LocalDate fin = LocalDate.parse(txtDateFin.getText(), formatter);

            modelTopProduits.setRowCount(0);

            Map<String, Integer> top = calculTopProduits(debut, fin);

            top.entrySet().stream()
                .sorted(Map.Entry.<String, Integer>comparingByValue().reversed())
                .forEach(e -> modelTopProduits.addRow(
                    new Object[]{e.getKey(), e.getValue()}
                ));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Dates invalides");
        }
    }                                              

    
